def _powershell_array(values: list[str], indent: int = 12) -> str:
    prefix = " " * indent
    lines = [prefix + "'" + value.replace("'", "''") + "'" for value in values]
    return ",\n".join(lines)


def _powershell_single_quoted_literal(value: str) -> str:
    """Return *value* as a PowerShell single-quoted string literal."""
    return "'" + str(value).replace("'", "''") + "'"


